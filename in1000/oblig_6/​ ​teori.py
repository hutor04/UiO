#1. Encapsulation restricts access to certain areas of a program and exposes only public interface to the user.
# The user has no possibility to change data or behaviour of the class or its instances without using the public interface.
# It is useful, because the users don't have to know how the encapsulated part of code works. The user only needs
# to provide the correct input data and s/he will get the predictable output.
# 2. User interface are the public methods that are available to the user. The users are not supposed to know anything
# about the implementation. There may exist private methods, that are not part of public interface.
# 3. Instance method are methods that can be called on an instance of a class. Unlike independent functions or procedures,
# instance methods can operate with data that is contained in the class (class or instance variables).
